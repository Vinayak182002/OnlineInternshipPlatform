    public void displayAllRecruiters()
    {
        try {
            //fetching all the Internships that have been posted!

            Class.forName("oracle.jdbc.OracleDriver");
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","12345");
            PreparedStatement st;
            st = con.prepareStatement("select recruiter_id,hr_name,hr_email,company_name,registration_no,company_website,company_desc,company_size,company_city,company_state,company_country,linkedIn_url from recruiter_profile");
            ResultSet rs=st.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            int cols = rsmd.getColumnCount();
            String []colNames = new String[cols];
            for(int i=0;i<cols;i++)
            {
                colNames[i] = rsmd.getColumnName(i+1);
            }
            model.setColumnIdentifiers(colNames);
            while(rs.next())
            {
                String []row = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9),rs.getString(10),rs.getString(11),rs.getString(12)};
                model.addRow(row);
            }
            theader1();
            st.close();
            con.close();
        } 
        catch (Exception e) {
           JOptionPane.showMessageDialog(null, e);
        }
    }
    
