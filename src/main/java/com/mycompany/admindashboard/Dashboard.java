    public void displayAllApplicants()
    {
        try {
            //fetching all the Internships that have been posted!

            Class.forName("oracle.jdbc.OracleDriver");
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","12345");
            PreparedStatement st;
            st = con.prepareStatement("select applicant_id,applicant_email,applicant_name,gender,dob,phone_no,linkedIn_url,github_url,portfolio_url from applicant_profile");
            ResultSet rs=st.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            
            int cols = rsmd.getColumnCount();
            String []colNames = new String[cols];
            for(int i=0;i<cols;i++)
            {
                colNames[i] = rsmd.getColumnName(i+1);
            }
            model.setColumnIdentifiers(colNames);
            while(rs.next())
            {
                String []row = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9)};
                model.addRow(row);
            }
            theader();
            st.close();
            con.close();
        } 
        catch (Exception e) {
           JOptionPane.showMessageDialog(null, e);
        }
    }
